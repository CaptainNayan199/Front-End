// A code that runs multiple times based on a conditions.
// Loops alter after the condition is fullfilled.
// types of loops : for, while, do while
// Iteration is the number of times a loop runs.


//For loops
for (let i = 0; i <= 10; i++) { // starts from 0 and ends at 10 and increments by 1 each every time.
  //console.log(i)
}


// while loops
let j = 10
while (j >= 0) {
  //console.log(j)
  j--
}



// do while - the condition inside do at first always executes despite of condition.

let k = 10
do {
  console.log(k * k)
  k++
} while (k <= 20)